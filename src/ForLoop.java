public class ForLoop {
    //Цикл for,(счетчик i;условие, до каких пор будет выполняться цикл; измение i,во время прохождения цикла)
            // Ctrl+/ добавить коментарии
    public static void main(String[] args) {
        for (int i = 15; i >= 10; i--) {
            System.out.println("Hello " + i);

        }

        for (int i = 0; i <= 4; i++) {
            System.out.println("hi "+i);/*Ctrl+/+Shift*/
//
        }
    }
}
